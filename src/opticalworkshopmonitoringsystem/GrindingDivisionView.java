    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
           try {
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            entityManager.getTransaction().begin();
            List<opticalworkshopmonitoringsystem.Grinding> merged = new ArrayList<opticalworkshopmonitoringsystem.Grinding>(grindingList.size());
            for (opticalworkshopmonitoringsystem.Grinding g : grindingList) {
                merged.add(entityManager.merge(g));
            }
            grindingList.clear();
            grindingList.addAll(merged); 
        }   
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         int[] selected = jTable1.getSelectedRows();
        List<opticalworkshopmonitoringsystem.Grinding> toRemove = new ArrayList<opticalworkshopmonitoringsystem.Grinding>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            opticalworkshopmonitoringsystem.Grinding g = grindingList.get(jTable1.convertRowIndexToModel(selected[idx]));
            toRemove.add(g);
            entityManager.remove(g);
        }
        grindingList.removeAll(toRemove); 
    }//GEN-LAST:event_jButton2ActionPerformed

